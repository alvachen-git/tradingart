COMMON_ALIASES.update(EXTRA_INDEX_MAP)


def resolve_symbol(query):
    """
    核心解析函数：智能识别 主力连续 vs 具体合约 vs 指数 vs 股票
    返回: (target_code, asset_type)
    asset_type 可能为: 'future', 'stock', 'index'
    """
    if not query or not isinstance(query, str) or query.strip() == "":
        return None, None

    query = str(query).strip().upper() # 转大写，去空格

    # --- 0. 优先检查是否直接输入了指数代码 ---
    if query in INDEX_CODES_SET:
        return query, 'index'

    # --- 1. 尝试匹配具体期货合约 (如 "豆粕2505", "rb2510") ---
    # 规则：中文/英文前缀 + 3-4位数字
    digit_match = re.search(r"(\d{3,4})", query)
    if digit_match:
        # 只有当后面确实跟着数字时才处理
        number_part = digit_match.group(1)
        prefix_part = query[:digit_match.start()].strip()

        # 🔥【核心修复】构建一个全小写的 values 列表进行比对
        all_values_lower = [str(v).lower() for v in COMMON_ALIASES.values()]

        # 情况1: 前缀就在 Keys 里 (因为我们在修改1里把 IM 加进去了)
        if prefix_part in COMMON_ALIASES:
            base_code = COMMON_ALIASES[prefix_part]
            if '.' not in base_code:
                return f"{base_code.upper()}{number_part}", 'future'

        # 情况2: 前缀在 Values 里 (兼容大小写)
        if prefix_part.lower() in all_values_lower:
            return f"{prefix_part.upper()}{number_part}", 'future'

    # --- 2. 查常用字典 (主力连续 / ETF / 指数 / 股票简称) ---
    if query in COMMON_ALIASES:
        code = COMMON_ALIASES[query]

        # 🔥【关键修改】判断类型
        if code in INDEX_CODES_SET:
            return code, 'index'  # 命中指数白名单
        elif '.' in code:
            return code, 'stock'  # 股票/ETF
        else:
            return code, 'future'  # 期货

    # 检查是否直接输入了期货品种代码 (如 'M', 'RB')
    # 🔥【核心修复】同样使用全小写比对
    if query.lower() in [str(v).lower() for v in COMMON_ALIASES.values()]:
        return query.upper(), 'future'

    # --- 3. 查全市场字典 (股票/ETF) ---
    market_map = get_all_market_map()
    if query in market_map:
        # 全市场字典里大概率没有指数，主要是股票和ETF
        return market_map[query], 'stock'

    # 模糊匹配 (名称包含)
    for name, code in market_map.items():
        if query in name:
            return code, 'stock'

    # 4. 匹配纯数字代码 (股票/ETF)
    # 如果用户输了 600519，默认是股票；如果输了 000300，可能是指数也可能是股票(很少见)
    # 这里做一个简单的兜底
    # --- 2. 纯数字代码自动补全 (如 600519) ---
    if query.isdigit() and len(query) == 6:
        # 沪市 (6开头)
        if query.startswith(('6', '5', '9')):
            return f"{query}.SH", "stock"
        # 深市 (0或3开头)
        elif query.startswith(('0', '3', '1', '2')):
            return f"{query}.SZ", "stock"
        # 北交所 (4或8开头)
        elif query.startswith(('4', '8')):
            return f"{query}.BJ", "stock"
        # 兜底默认
        else:
            return f"{query}.SH", "stock"

        # 🔥【关键新增】港股通常是 5位 (如 00700)
        if len(query) == 5:
            # 尝试补全 .HK
            hk_code = f"{query}.HK"
            # 只要在数据库里见过这个代码，就返回
            if query in market_map: return market_map[query], 'stock'
            # 或者直接返回
            return hk_code, 'stock'

        # 再去股票列表找
        for code in market_map.values():
            if query == code.split('.')[0]:
                return code, 'stock'

    return None, None


